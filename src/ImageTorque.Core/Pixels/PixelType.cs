namespace ImageTorque.Pixels;

/// <summary>
/// The pixel type.
/// </summary>
public enum PixelType
{
    /// <summary>
    /// Single-precision floating point lumincance pixel.
    /// </summary>
    LS,
    /// <summary>
    /// 8-bit luminance pixel.
    /// </summary>
    L8,
    /// <summary>
    /// 16-bit luminance pixel.
    /// </summary>
    L16,
    /// <summary>
    /// Single-precision foating pint color pixel.
    /// </summary>
    Rgb,
    /// <summary>
    /// 24-bit color pixel.
    /// </summary>
    Rgb24,
    /// <summary>
    /// 48-bit color pixel.
    /// </summary>
    Rgb48
}
