namespace ImageTorque;

/// <summary>
/// The pixel format.
/// </summary>
public enum PixelFormat
{
    /// <summary>
    /// Unknown.
    /// </summary>
    Unknown,
    /// <summary>
    /// Mono single.
    /// </summary>
    Mono,
    /// <summary>
    /// Mono 8 bit.
    /// </summary>
    Mono8,
    /// <summary>
    /// Mono 16 bit.
    /// </summary>
    Mono16,
    /// <summary>
    /// RGB packed.
    /// </summary>
    RgbPacked,
    /// <summary>
    /// RGB 24 packed.
    /// </summary>
    Rgb24Packed,
    /// <summary>
    /// RGB 48 packed.
    /// </summary>
    Rgb48Packed,
    /// <summary>
    /// RGB planar.
    /// </summary>
    RgbPlanar,
    /// <summary>
    /// RGB 888 planar.
    /// </summary>
    Rgb888Planar,
    /// <summary>
    /// RGB 161616 planar.
    /// </summary>
    Rgb161616Planar
}
